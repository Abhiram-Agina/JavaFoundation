package OperatorsStatements;

public class ModulusVSDivision {
	public static void main(String[] args) {
		
		System.out.println(9/3); // returns 3
		System.out.println(9%3); // returns 0
		
		System.out.println(10/3); // returns 3
		System.out.println(10%3); // returns 1
		
		System.out.println(11/3); // returns 3
		System.out.println(11%3); // returns 2
		
		System.out.println(12/3); // returns 3
		System.out.println(12%3); // returns 2
		
	}
}
